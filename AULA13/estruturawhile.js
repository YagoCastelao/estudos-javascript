//Acima temos o modo pouco prático, porque temos que ficar copiando várias vezes o mesmo comando!

var c = 1
while (c <= 6) {
    console.log(`Passo ${c}`)
    c++ // c++ significa c +1, ou seja, enquanto a váriavél c for menor que 6 o sistema vai repetir o comando, sem que precisamos ficar copiando o mesmo código várias vezes
}

